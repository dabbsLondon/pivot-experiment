CREATE DATABASE IF NOT EXISTS pivot;

CREATE TABLE IF NOT EXISTS pivot.trades_1d
(
    trade_date Date,
    ts DateTime64(3),
    portfolio_manager_id UInt32,
    fund_id UInt32,
    portfolio_id UInt64,
    account_id UInt64,
    desk LowCardinality(String),
    book LowCardinality(String),
    strategy LowCardinality(String),
    region LowCardinality(String),
    country LowCardinality(String),
    venue LowCardinality(String),
    asset_class LowCardinality(String),
    product LowCardinality(String),
    symbol String,
    currency LowCardinality(String),
    counterparty String,
    risk_bucket LowCardinality(String),
    scenario LowCardinality(String),
    trade_id UInt64,
    order_id UInt64,
    quantity Float64,
    price Float64,
    notional Float64,
    pnl Float64,
    delta Float64,
    gamma Float64,
    vega Float64,
    theta Float64,
    rho Float64,
    margin Float64,
    fees Float64,
    slippage Float64,
    vol Float64,
    rate Float64,
    exposure Float64,
    metric_1 Float64,
    metric_2 Float64,
    metric_3 Float64,
    metric_4 Float64,
    metric_5 Float64,
    metric_6 Float64,
    metric_7 Float64,
    metric_8 Float64,
    metric_9 Float64,
    metric_10 Float64,
    metric_11 Float64,
    metric_12 Float64,
    metric_13 Float64,
    metric_14 Float64
)
ENGINE = MergeTree
PARTITION BY trade_date
ORDER BY (portfolio_manager_id, fund_id, book, asset_class, symbol, ts);
